#include<iostream>
using namespace std;

int main(){
	int a[37]={1,2,4,8,16,32,64,128,256,512,1024,2048,4096,1+4,1+8,2+4,2+32,4+8,4+32,4+64,8+16,8+64,8+128,16+128,32+64,32+256,32+512,64+128,64+512,64+1024,128+1024,128+2048,256+512,512+1024,512+4096,1024+2048,1024+4096}, dp[20000]={}, i;
	dp[0] = 1;
	int j;
	for(i=0;i<4096*2;i++){
		for(j=0;j<37;j++){
			if((i&a[j])==0&&dp[i+a[j]]==0){
				if(dp[i] == 1)
					dp[i+a[j]] = 2;
				else
					dp[i+a[j]] = 1;	
			}
			if((i&a[j])==0&&dp[i]==2){
				dp[i+a[j]] = 1;	
			}
		}
	}
	while(cin >> i){
		while(i--){
			cin >> j;
			if(dp[j] == 1)
				cout << 1 << " ";
			else
				cout << 0 << " ";	
		}
		cout << endl;
	}
}

